import fs from 'fs';
import path from 'path';

const deploymentPath = path.join(process.cwd(), 'deployment', 'sepolia.json');
const outDir = path.join(process.cwd(), 'src', 'generated');
const outPath = path.join(outDir, 'deployment.ts');
fs.mkdirSync(outDir, { recursive: true });

let deployment = {
  chainId: 11155111,
  network: 'sepolia',
  contract: 'PrivateScratchRegistry',
  address: '',
  explorer: '',
  deployedAt: '',
};

if (fs.existsSync(deploymentPath)) {
  const parsed = JSON.parse(fs.readFileSync(deploymentPath, 'utf8'));
  deployment = {
    chainId: Number(parsed.chainId || 11155111),
    network: parsed.network || 'sepolia',
    contract: parsed.contract || 'PrivateScratchRegistry',
    address: parsed.address || '',
    explorer: parsed.explorer || '',
    deployedAt: parsed.deployedAt || '',
  };
}

fs.writeFileSync(outPath, `// Auto-generated by scripts/export-frontend-config.mjs. Safe to commit.\nexport const DEPLOYMENT = ${JSON.stringify(deployment, null, 2)} as const;\n`);
console.log('Wrote', outPath);
